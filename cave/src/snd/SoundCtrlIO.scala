package cave.snd

import arcadia.mem._
import chisel3._

/** An bundle that contains control signals for the sound PCB. */
class SoundCtrlIO extends Bundle {
  /** OKIM6295 CPU port */
  val oki = Flipped(Vec(Sound.OKI_COUNT, MemIO(23, 16)))
  /** NMK115 CPU port */
  val nmk = Flipped(WriteMemIO(23, 16))
  /** YMZ280B CPU port */
  val ymz = Flipped(MemIO(23, 16))
  /** Request flag */
  val req = Input(Bool())
  /** Request data */
  val data = Input(Bits(16.W))
  /** IRQ */
  val irq = Output(Bool())
}

object SoundCtrlIO {
  def apply(): SoundCtrlIO = new SoundCtrlIO()
}
